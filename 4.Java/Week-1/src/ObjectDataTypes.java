public class ObjectDataTypes {
  public static void main(String[] args) {
    
    Byte c2byte = 8;
    System.out.println("Variable byte object : " + c2byte);
    
    Short c2Short = 16;
    System.out.println("Variable short object : " + c2Short);
    
    Integer c2int = 32;
    System.out.println("Variable int object : " + c2int);
    
    Long c2long = 64L;
    System.out.println("Variable  long object : " + c2long);
    
    Float c2float = 128.128f;
    System.out.println("Variable float object: " + c2float);
    
    Double c2double = 254.254;
    System.out.println("Variable double object: " + c2double);
    
    Character c2char = 5686;
    System.out.println("Variable char object: " + c2char);
    
    Boolean c2bool = true;
    System.out.println("Variable boolean object: " + c2bool);
  }
}